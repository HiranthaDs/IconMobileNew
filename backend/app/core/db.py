SEED_META = [
    ("schema_version", None),  # filled from SCHEMA_VERSION at init time
    ("revision", "0"),
    ("backup_interval_days", "7"),
    ("automatic_backup_interval_hours", "24"),
]


def init_db(schema_version: str) -> None:
    """Create every table/index if missing.  Idempotent; safe on each startup."""
    handle = get_pool()
    raw = handle.getconn()
    try:
        raw.autocommit = True
        with raw.cursor() as cursor:
            for statement in SCHEMA_STATEMENTS:
                cursor.execute(statement)
            cursor.execute(
                "INSERT INTO v2_meta(key, value) VALUES(%s, %s) ON CONFLICT (key) DO NOTHING",
                ("schema_version", schema_version),
            )
            for key, value in SEED_META:
                if value is None:
                    continue
                cursor.execute(
                    "INSERT INTO v2_meta(key, value) VALUES(%s, %s) ON CONFLICT (key) DO NOTHING",
                    (key, value),
                )
    finally:
        raw.autocommit = False
        handle.putconn(raw)
