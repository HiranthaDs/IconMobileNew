"""Supabase/Postgres connection layer for the ICON MOBILE store.

The original application used ``sqlite3`` with a ``conn.execute(sql, params)``
API and ``?`` placeholders.  To keep the ported store logic faithful and easy
to compare against ``backend_store.py``, this module exposes a thin
``PgConnection`` wrapper with the same ``.execute()`` shape on top of
``psycopg2``.  The ported SQL uses ``%s`` placeholders (psycopg2) and relies on
the ``citext`` extension for the case-insensitive unique columns that were
``TEXT COLLATE NOCASE`` in SQLite.
"""

from __future__ import annotations

import os
import threading
from contextlib import contextmanager
from pathlib import Path
from typing import Any, Iterator, Optional, Sequence

import psycopg2
from dotenv import load_dotenv
from psycopg2 import extras, pool

load_dotenv(Path(__file__).resolve().parents[3] / ".env")

_pool: Optional[pool.ThreadedConnectionPool] = None
_pool_lock = threading.Lock()


def _dsn() -> str:
    dsn = os.environ.get("SUPABASE_DB_URL", "").strip()
    if not dsn:
        raise RuntimeError(
            "SUPABASE_DB_URL must be set to the Supabase Postgres connection "
            "string (Project Settings -> Database -> Connection string -> URI)."
        )
    return dsn


def get_pool() -> pool.ThreadedConnectionPool:
    global _pool
    with _pool_lock:
        if _pool is None:
            _pool = pool.ThreadedConnectionPool(1, 12, dsn=_dsn())
    return _pool


class _Empty:
    """Return value for BEGIN/COMMIT/ROLLBACK so callers can chain safely."""

    rowcount = 0
    lastrowid = None

    def fetchone(self) -> Any:
        return None

    def fetchall(self) -> list:
        return []


class PgConnection:
    """psycopg2 adapter with a sqlite3-style ``.execute()`` method.

    ``execute("COMMIT")`` / ``execute("ROLLBACK")`` map to the real transaction
    controls; ``execute("BEGIN")`` is a no-op because psycopg2 opens a
    transaction implicitly on the first statement.  Every other statement runs
    through a ``DictCursor`` whose rows support both ``row[0]`` and
    ``row["column"]`` access, matching ``sqlite3.Row``.
    """

    def __init__(self, raw: Any) -> None:
        self.raw = raw

    def execute(self, sql: str, params: Sequence[Any] = ()) -> Any:
        control = sql.strip().upper()
        if control == "COMMIT":
            self.raw.commit()
            return _Empty()
        if control == "ROLLBACK":
            self.raw.rollback()
            return _Empty()
        if control in ("BEGIN", "BEGIN IMMEDIATE"):
            return _Empty()
        cursor = self.raw.cursor(cursor_factory=extras.DictCursor)
        cursor.execute(sql, params)
        return cursor


@contextmanager
def connection(*, read_only: bool = False) -> Iterator[PgConnection]:
    handle = get_pool()
    raw = handle.getconn()
    raw.autocommit = False
    try:
        yield PgConnection(raw)
        raw.commit()
    except Exception:
        raw.rollback()
        raise
    finally:
        handle.putconn(raw)


# ---------------------------------------------------------------------------
# Schema.  Translated from the SQLite v2 schema in backend_store.py:
#  * INTEGER PRIMARY KEY  -> BIGINT GENERATED BY DEFAULT AS IDENTITY
#    (BY DEFAULT lets a restore re-insert explicit ids)
#  * TEXT COLLATE NOCASE UNIQUE -> CITEXT UNIQUE (case-insensitive equality)
#  * PRAGMA / WAL removed; Postgres enforces foreign keys natively
# ---------------------------------------------------------------------------

